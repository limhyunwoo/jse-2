package cmm02.op;

public class No04_conditionDemo {
	public static void main(String[] args) {
		int val01 = 1;
		int val02 = 2;
		
		if((val01 == 1) && (val02 == 2))
			System.out.println("1번값과 2번값은 같습니다.");
		
		if((val01 != 1) || (val02 != 2))
			System.out.println("1번값과 2번값은 다릅니다.");
			
	}
}
